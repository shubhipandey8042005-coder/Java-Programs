public class Arr_swap
{
	public static void main(String args[])
	{
		int i=10;
		int j=5;
		int temp;
		System.out.println("Before swapping:");
		System.out.println("i value is:"+i);
		System.out.println("j value is:"+j);
		temp=i;
		i=j;
		j=temp;
		System.out.println("After swapping:");
		System.out.println("i value is:"+i);
		System.out.println("j value is:"+j);
		
	}
}